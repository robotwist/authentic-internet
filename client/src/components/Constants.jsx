export const isWalkable = (x, y, map) => {
  const row = Math.floor(y / TILE_SIZE);
  const col = Math.floor(x / TILE_SIZE);
  return map?.[row]?.[col] === 0 || map?.[row]?.[col] === 5 || map?.[row]?.[col] === 3; 
};

